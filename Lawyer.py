class Lawyer:

    def __init__(self, name, last_name, email, address):
        self.name = name
        self.last_name = last_name
        self.email = email
        self.address = address

    def get_name(self):
        return self.name

    def get_last_name(self):
        return self.last_name

    def get_email(self):
        return self.email

    def get_address(self):
        return self.address
